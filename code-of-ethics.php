<p class="wow fadeInUp" data-wow-delay="0.2s"><b>CONDITIONS OF SERVICE FOR STAFF MEMBERS </b><br>

All staff members in full employment (here in after called employees) shall be subject to the under listed rules and regulations. 

<ul>

<li>    EMPLOYMENT <br>

a)  Health Issues 
In the interest of health, employees before and during employment of the school will be required to submit to a medical examination by a medical officer in charge, Korle-Bu teaching hospital or any other officer appointed by the board of trustee (DIRECTORS) of the school.  There will be an internal health screening ones every two years by any medical laboratory appointed by management of the school which the employee will bear the cost.  <br>

b)  Upon engagement, the employee will be placed on probation for ONE YEAR. If the employee proves unsuitable, his or her appointment will be terminated during or at the end of the trial period. Employees will devote their time and to their jobs for which they have been appointed by the board of trustees and shall not be engaged in additional trade or business which will conflict indirectly or directly with the schools interest or objectives. <br>

c)  No guarantee will be given that a teacher returning from unpaid leave or first appointment salary will be paid from the day on which duty is commenced. <br>

d)  Salary is fixed by the Board of Trustee and is subject to amendment of which adequate notice will be given. The point of entry on the salary scale will be determined on previous recognized teaching experience and qualification. <br>

e)  The salary scale is based on the fact that the appointment is a full time one. All teachers are expected to take due share of the schools activities. </li> <br>

<li>    HOURS OF WORK <br>
All employees are required to be at school at designated hours from 6:55am – 4pm at the Primary Dept. and 6:45am – 3:00pm at the JHS Dept. no employee shall leave work without permission during working hours. </li> <br>

<li>    MATERNITY LEAVE <br>
To qualify for maternity leave, a female employee must have her appointment confirmed prior to the date of application to such leave. Maternity leave will be six weeks leave before and six weeks after conferment with full payment provided that such entitlement will apply on three times and during her period of employment. <br>
At least three months written notice must be given to the director indicating the date of recommended leave. The leave pay for three months will be paid if only the teacher returns to post or withheld in lieu of notice if she fails to return. <br>
Two years working after confirmation, before a female employee will be entitled to full maternity leave pay. The female employee will close 2:00pm each working day after resume from her maternity leave. </li> <br>

<li>SOCIAL SECURITY AND NATIONAL INSURANCE TRUST <br>
All employees are mandated to be placed under the Social Security and National Insurance Pension Scheme and the appropriate deductions from the employee’s salary, will be made in accordance with the Social Security Law 1996(Act 766) except where disqualified by age or otherwise, 5.5% will be deducted from the employee’s salary and the payment of 13% of the employee’s salary will be paid by the school to sum up to 18.5% of the social security as statutory demands.   </li> <br>

<li>    STUDY LEAVE <br>
No guarantee can be given that an employee returning from unpaid leave of any kind will be re-engaged. The CEO according to the prevailing needs of the school will determine re-engagement of an employee who has worked effectively will be re-engaged.
A written notice of one month should be given to the school by an employee before he or she proceeds for study leave. </li> <br>

<li>    TERMLY REPORTS/ PERFORMANCE APPRAISAL <br>
A termly report will be made on each employee at the end of end of each term. Each employee will be given the opportunity to read his/ her reports and make comments thereof. The Board shall deem it fit to form a three member committee to be present during the evaluation. </li> <br>


<li>    GRIEVANCES. <br>
An employee who has any grievances or feels he / she has been subjected to an unfair treatment, may complain in writing to the Chief Executive Officer (C.E.O). </li>
<br>

<li>    INJURY COMPENSATION <br>
All cases of accident and injury incurred in the course of duty must be reported immediately for action. The school authorities in accordance with the workman’s compensation Law 187 (PNDC 132) may pay compensation according to the cause of injury. </li>
<br>

<li>    INCREMENT OF SALARY <br>
Increment of salary will be based on satisfactory performance by the individual employee after a thorough appraisal has been done and approved by the board. </li> <br>

<li>    TERMINATION OF APPOINTMENT <br>
Where through force of circumstances, it becomes necessary to reduce staff, the board of directors shall take into account, efficiency, and length of service, diligence, loyalty and health.  
Termination of this appointment shall be preceded by the notification period of 30 days. </li>

</ul>
</p>


