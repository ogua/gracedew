                    CODE OF ETHICS FOR EMPLOYEE

<p class="wow fadeInUp" data-wow-delay="0.2s"> <b>TEACHING NOTES </b> <br>
A teacher shall prepare relevant and adequate scheme of work and teaching notes for his / her work in advance. It shall be the responsibility of the head of the institution to see to it that this is done. </p>

<p class="wow fadeInUp" data-wow-delay="0.2s">
<b>EXERCISE <b>
<ul>
<li>A teacher shall be given a target by the management. He is expected to give adequate amount of written and practical exercises and homework in all subjects that he / she teaches.</li>
<li>    A teacher shall mark and evaluate all written /practical exercises promptly and carefully.</li>
<li>    A teacher shall not make derogatory remarks in the exercise books of the child. </li>
</ul>

</p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>WORKING HOURS</b
<ul>
<li>    An employee shall report for duty regularly and punctually as determined by the school 6:55.a.m – preschool to 5th grade and 6:45.a.m – 6th to 9th grade. </li>
<li>    Reporting and closing time will be determined by the management of the school. </li>
<li>    An employee may be required to work beyond the required time in certain circumstances which will be determined by the management of the school. </li>
<li>    A record of attendance shall be kept in the school and it shall be the responsibility of the head of the school to see to it that teachers / staffs sign in and out before leaving the school premises. </li>
</ul>
</p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>PERFORMANCE OF DUTY</b>
<ul>
<li>    An employee shall not fail to carry out his/her work in accordance with his / her profession, trade or occupation or in a manner determined by the school.</li>
<li>    An employee whose work performance is proven through staff appraisal to be below the set standard shall be warned and given all the necessary encouragement by indicating to him/her the improvement he/she is required to make within a reasonable time frame.</li>
<li>    After two warnings of proven poor performance without improvement the employee shall be sanctioned.</li>
<li>    Any employee who is found to have forged or falsified any document or impersonated any other person with intent of misleading the school shall be sanctioned.</li>
<li>    Any negligence on the part of an employee, which causes unacceptable loss, damage or injury, shall be a breach of contract of service.</li>
<li>    No teacher is to teach while sitting.</li>
</ul>
</p>


<p class="wow fadeInUp" data-wow-delay="0.2s"><b>PRIVATE ACTIVITIES DURING BUSINESS HOURS</b>
<ul>
<li>    No staff shall engage in private and personal conversation during class period when he/she is required to supervise children at work or play. </li>
<li>    No staff shall trade or transact any private financial business on the school premises during school hours. </li>
<li>    No staff shall perform unofficial duties or activities during school hours without permission from management. </li>
</ul>
</p> 



<p class="wow fadeInUp" data-wow-delay="0.2s"><b>CO-CURRICULAR</b> <br>
Teachers shall take part in approval co-curricular activities in the school. </p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>IMPROPER USE OF CHILDREN’S LABOUR </b>
<ul>

<li>An employee shall not send a child on errands for him / her during contact hours. </li>
<li>    No employee shall use the labour of school children. </li>

</ul>

</p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>PROTECTION OF CHILDREN FROM TORTURE AND OTHER DEGRADING TREATMENT</b>

<ul>

<li><b>Physical violence </b><br>
Consequently <br>
i.  An employee shall not administer any act of corporal punishment, or any act that inflicts physical pain on the children or causes physical harm to their pupils / students such as pushing, pulling, hitting and / flogging. <br>
ii. No staff shall punish a child to do any work outside the classroom during contact. <br>
iii.    A teacher shall not threaten any pupil/student with harm with intent to put that person in fear of harm. <br>
iv. No teacher shall assault any teacher or pupil. <br>
v.  A teacher shall intervene to stop a pupil / student from perpetrating physical violence or abuse upon another pupil or student. <br>
</li> <br>

<li><b>Psychological Violence </b> <br>
i.  No act of a teacher shall have a negative psychological effect on a school child. Therefore no teacher in the course of his duty shall intimidate, insult, tease, harass, threaten, snub or discriminate any child. <br>
ii. Teachers shall not use any acts or means to pester or coerce children into activities of which they do not have a clear understanding.
iii.    No teacher shall deliberately isolate or ignore any child. <br>
iv. No teacher shall emotionally manipulate a child to the teachers’ advantage. <br>
v.  No teacher shall send a child out for absenteeism or lateness. <br>
vi. The teacher shall control his / her utterances in order not to threaten with cruel and degrading punishment to hurt the child or pupil. <br>
vii.    A teacher will intervene to stop a pupil / student from perpetrating psychological abuse upon another pupil / students. <br>
viii.   A teacher will intervene to stop a fellow teacher from perpetrating psychological abuse upon another teacher. </li> <br>

<li>    <b>Sexual Violence </b> <br>
i.  No staff shall directly or indirectly do anything that will constitute sexual harassment of a pupil/student. <br>
ii. Any staff, who has carnal knowledge of any female or male pupil/student of any age, with without his or her consent, shall be guilty of professional misconduct. <br>
iii.     No staff shall compel any female or male pupil/student in his or in any educational institution to marry him or her without the consent of the child’s parents. <br>
iv. No staff shall detain any pupil / student for immoral purpose. <br>
v.  No teacher shall by force pretences or false representation procure any pupil/students to have any carnal knowledge with him / her. <br>
vi. A teacher shall not apply or administer to or cause to be taken by any pupil / student an illegal drugs, matter or thing with intent to stupefy or overpower him / her so as thereby to enable the teacher or any person to have carnal knowledge with such pupil/student. <br>
vii.     No teacher shall publicly or in secret willfully commit any act of decency towards any pupil/student. <br>
viii.   It shall be the responsibility of any teacher who directly or indirectly gets to know of the occurrence of any of these acts to report it to his / her superior. <br>
ix. A teacher will intervene to stop a pupil / student from perpetrating sexual abuse or violence over upon another pupil /student.
x.  No teacher shall directly or indirectly, instigate, aid, or in any manner facilitate, encourage or promote whether by his acts or presence or otherwise any of the above named acts. </li>
<br>

<li><b>Non – Discrimination </b> <br>
No employee shall discriminate against a pupil / student on the grounds of sex, race, age, religion, disability, health status, custom ethnic origin, back ground, socio-economic status or misunderstanding with a parent or guardian. </li> <br>

<li>    <b>Right to social activity </b> <br>
No teacher shall detain a pupil after school as a form of punishment without supervision. </li> <br>

<li><b>Favouratism </b> <br>
No teacher shall do anything that shall suggest or create the impression that a pupil / student are more favoured than another pupil/student. </li> <br>

<li><b>Inordinate Affection</b> <br>
No teacher shall under any circumstances show any form of inordinate affection to any pupil or students. Inordinate affection implies expression of love or likeness with ulterior.</li> <br>

<li><b>Role Model</b> <br>
i.  Teachers shall serve as role models to learners showing high degree of decency in speech, mannerism, discipline, dressing and general performance 
ii. Teachers shall in particular dress to portray the dignity of the profession.</li>  <br>

<li><b>Ideological influence</b> <br>
Teacher shall not use their position to spread the political, religious or other ideologies among people / students. </li> <br>

<li><b>Absence from duty </b> <br>
i.  No employee may leave the school during school hours without the permission of the head / the CEO of school.<br>
ii. Anyone leaving the school for duty else where shall inform his head and the CEO of his where about to facilitate his recall in an emergency.<br>
iii.    A teacher shall not absent himself / herself from work on grounds of ill-health without permission from his / her management and subsequent submission of a medical certificate from a certified medical practitioner if he / she has to be absent for days.<br> 
iv. Call the same day between 2:00pm – 4:00pm to let the CEO and Heads know if you will be able to report to work the next day.  <br>
v.  Excuse duty form to stay home or be absent must be brought to the school by, family member, friend or yourself.  A teacher shall not absent himself /herself from assigned work without permission.<br>
vi. It is misconduct for a teacher to absent him/herself from duty for one or more days continuously without permission or reasonable excuse or course. <br>
vii.Employee who absent him/herself from duty continuously for ten days or more shall be deem to have vacated post. </li> <br>

<li><b> Reporting Day </b><br>
Absenteeism letter should be brought to the office between 6:45am – 7:00am if not you will be marked absent. <br>

l.  Property of Employer <br>
i.  A teacher shall not cause a deliberate damage to the property of his / her employer. <br>
ii. A teacher shall not use the property of the employer without permission from the appropriate authority of his / her immediate head. <br>
iii.    No teacher shall use the name of the property of the employer for personal benefit or advantage. </li> <br>

<li><b> Protection of the interest of the Employer</b><br>
i.  A teacher shall not divulge privileged or classified information or documents to any person or body that is not entitled to have access to such information or document.  <br>
ii. No teacher shall engage in any other gainful economic activity at the workplace. <br>
iii.    No teacher shall in the course of his/her duties disobey, disregard or willfully default in carrying out any lawful instructions, reasonable orders or directives given by any person or body having authority to give such lawful instructions, reasonable orders or directives. </li>
<br>

<li><b> Insubordination </b><br>
No teacher shall in the course of his or her duties disobey, disregards or willfully default in carrying out any lawful instruction, reasonable order given by any person, committee or Board having authority to give such order or instruction. </li>
<br>

<li><b> Presence of babies in school </b><br>
Except where permission is granted for a teacher’s baby to be brought to school, no teacher shall bring babies or children under school going age to school. </li> <br>

<li><b> Use of illegal drugs in school receive </b><br>  
i.  No teacher shall use illegal drugs during work sessions or at the workplace. <br>
ii. No teacher shall involve a pupil/student by sending him / her to purchase illegal drugs. </li>

<br>
<li><b> Drinking, drunkenness and smoking </b><br>
i.  No teacher shall drink alcohol while on duty or be found drunk during school hours. <br>
ii. No teacher shall smoke in the classroom during school hours or in a place within the school premises. </li> <br>

<li><b> Public Examination </b><br>
i.  No teacher acting as an invigilator or supervisor shall offer assistance to candidates at internal or external examinations with the intent to cheat. <br>
ii. No teacher shall leak internal or public examination questions to any candidate/person.  <br>
iii.    Any alleged criminal act or omission at the work place by a teacher such as stealing, falsification of records or a deliberate misrepresentation of facts is a gross misconduct. </li> <br>

<li><b> Criminal Acts/ Omissions </b><br>
i.  For the purpose of this code, criminal acts and omissions are classified into two, these are <br>
a.  Criminal acts or omissions outside the work place: and  <br>
b.  Criminal acts or omissions at the work place: <br>
ii. Any alleged Criminal acts or omissions by a teacher outside the work place that renders, in the opinion of the employer, the teacher unsuitable for his or her present job may provide basis for disciplinary action. <br>
iii.    Any alleged Criminal acts or omissions at the work place by a teacher such as stealing, falsification of records or a deliberate misrepresentation of facts is a gross misconduct. </li><br>

<li><b> Official correspondence or records </b><br>
No teacher shall show or take official correspondence or records to private persons, corporations, companies, or other bodies without the express and prior consent of the C.E.O </li> <br>

<li><b>Anonymous letter </b><br>
No teacher shall write or circulate anonymous letters with malicious intent. </li> <br>

<li><b>Acts of Dishonour </b><br>
I.  No teacher shall involve himself/herself in any act that is likely to bring the teaching profession into disrepute. <br>
II. It shall therefore be the responsibility of every teacher to preserve the dignity and honour of his profession and also maintain his / her own dignity, honour and integrity. </li> <br>

<li><b> Prompt Action on disciplinary matters </b><br>
All acts of misconduct by a teacher shall be dealt with promptly. </li>

</ul>
</p>


<p class="wow fadeInUp" data-wow-delay="0.2s"><b>REPORTING TIME </b><br>
All teachers (Pre-school department, Primary department and non teaching staff) are to report before 6:55 a.m. 
Teachers at the J.H.S department are to report before 6:45 a.m.  </p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>Teacher on duty</b><br>
Every teacher on duty is to report to school before 6:40 a.m. and leave the school 5:00 p.m. at the pre-schools, primary and J.H department.</p> 

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>Clocking Machine</b><br>
Every employee is support to clock in the morning when he / she report to work and clock out after work. Anyone who refuses to clock will be mark absent for the day.</p>


<p class="wow fadeInUp" data-wow-delay="0.2s"><b>DEDUCTIONS </b><br>
Late Coming
<ul>
<li>    Any employee who reports late to work three or more times in a month, will have Fifty Ghana Cedis (GHC50.00) deducted from his / her salary after having been given a query letter. </li>

<li>    One hundred Ghana Cedis (GHC100.00) will be deducted when the late coming become habitual.
Absence from work  </li>
<li>    Excuse duty form to stay home or be absent must be brought to the school by, a family member, friend or yourself.  Anyone who fails to call or to bring an excuse duty form, will have GHC50.00 deducted from his / her salary. </li>

<li>    An absence letter should be brought to the office between 6:45am – 7:00am when you report to work, if not you will be marked absent for the day and GHC50.00 will be deducted from your salary </li>
</ul>
</p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>Caning</b><br> Any teacher who uses the cane indiscriminately shall be suspended for three working days without pay. </p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>Use of Phone</b><br>
Fifty Ghana Cedis (GHC50.00) shall be deducted from the salary of any staff member who is caught using the phone during working hours.
Members of staff should take note of these rules and regulations and comply.
Any staff member who has reservations about any of the sanctions above can resign. </p>

<p class="wow fadeInUp" data-wow-delay="0.2s"><b>EXPLANATORY NOTES ON TYPES OF PENALTY</b> <br>
DISMISSAL <br>
An Employee may be dismissed on the following:
<ul>
<li>    When an employee is proved through staff appraisal to be incompetent. </li>
<li>    When an employee is seen not to have possessed a qualification he / she claimed. </li>
<li>    When misconduct of an employee is a breach of a fundamental clause in the terms of contract of service (e.g. stealing of employer’s property). </li>
<li>    When the employer has a reasonable belief in the guilt of an employee alleged to have committed a criminal offence at the work place. </li>
<li>    When an employee has been given a query three times on the same situation / issues / habit e.g. habitual late comer. </li>
</ul>
</p>


