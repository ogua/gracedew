<h3 class="mt-5">ATTENDANCE</h3>
<p>Regular attendance is essential to the progress and achievement of every child. Thus: <br>
 (1) Student absenteeism should only occur for necessary and legitimate reasons. Such as Illness, Medical appointments. <br>
 (2) Parents should arrange travel and vacation time to coincide with scheduled holidays and vacations as noted on the school’s calendar. <br>
(3) Excused absence: <br>
● Parents have to notify the school’s administrator by phone or e-mail before the day of absence, explaining the reason for absence. <br>
● On arrival to school, the student will be advised of missed assignments. <br>
● All assignments must be completed in the amount of time specified by the teacher(s). <br>
(4) For extended and planned absence, parents must provide a one week notice to the school’s administration. Parents of such students must consult with the school principal or administrator and classroom teacher in regard to child’s ability to progress academically following such an absence. <br>
(5) If a student is truant, the school administrator will contact parents and schedule a meeting with the class teacher and the School Director before student is readmitted to the class. <br>
(6) If a student absents his/herself for 10 or more days within a term, he or she automatically fails that term. If a student is absent for 45 school days (25%) in a given school year , the student may not be promoted to the next grade level. In extreme cases, parents may be advised to withdraw the student from the school. The noted limits to absenteeism also apply to individual subjects. <br>
(7) If the absence is due to a prolonged illness and the student has evidence that he/she has made up for the missed material to the satisfaction of the teachers, then an appeal may be made to the School Director. <br>
(8) In all attendance matters, the decision of the School Director is final.
</p>

<h3 class="mt-5">DRESS CODE</h3>
<p>All students of Wonder World International School are expected to abide by all rules and regulations. There are no exceptions.</p>

<h3 class="mt-5">Uniforms</h3>
<p>Uniforms should be neat, clean, and in good repair. Students’ personal appearance should reflect a respect for themselves, their school and the community. Students are expected to dress in a manner that is not distracting to the teachers and/or other students. Students are expected to wear the school uniform every day. We have a strict dress code; any child who has a valid reason not to be in the correct uniform should provide a written excuse, signed by the parent/guardian to the effect. There is no provision for any additions to school uniforms such as scarves, bandanas, wristbands, etc. In cold or wet weather, students may wear pull over or cardigan over their uniforms. Hoodies are not permitted in the school.</p>

<h3 class="mt-5">Hair</h3>
<p>Girls are not allowed to wear beads in their hair. Natural hair must be neatly kept and all long braided or natural/permed hair must always held in a ponytail with a ribbon or band; the hair should not be left hanging. For boys, hair must be kept neat, short and even on all sides. No Hi-top fade haircuts, dyeing of hair, braids or extravagant designs.</p>

<h3 class="mt-5">Comfortable shoes – Black/Brown</h3>
<p>Students should wear well fitted closed toe shoes, laced and tied, or sandals with a strap in the back. Black sandals or flat shoes (absolutely no heels) worn with white socks. Socks can be ankle or knee high. Shoes must conform to the dress code. Bare feet, flip flops and crocs are not allowed.</p>

<h3 class="mt-5">Jewelry/Make-up</h3>
<p>No jewelry is permitted except simple studs for girls with pierced ears (no chains, bangles, anklets, hoops, wrist bands, etc) Boys are not permitted to wear anything in their ears or hands. No make-up is permitted.</p>

<h3 class="mt-5">Hats, Caps, Headwear, and Sunglasses</h3>
<p>Headwear and sunglasses may not be worn in the school premises</p>

<h3 class="mt-5">Nails</h3>
<p>Students’ nails must be short, clean, with no nail polish, and well-trimmed.</p>

<h3 class="mt-5">Kitchen Service (Breakfast, Morning snack, Lunch, Afternoon snack</h3>
<p>The school’s kitchen serves hot meals to students for breakfast and lunch. The first snack served every day after breakfast is fruit, the second snack is dependent on what the menu states</p>

<h3 class="mt-5">Relocation/Change of Telephone numbers</h3>
<p>Parents should ensure that the school has their most up-to-date contact information when they have enrolled in the school. Contact details include: numbers, parents’ work numbers, cell numbers, e-mail addresses, and an emergency number (e.g. a close relative). If they relocate, Parents are responsible for contacting the school for any due changes in contact information to be made.</p>

<h3 class="mt-5">Emergency SMS Message Alert System/WhatsApp</h3>
<p>The School has a massaging system which is activated to send messages concerning critical/emergency situation about student to parents. It is critical that all parents ensure that their mobile phone numbers in the school records are kept up to date for these systems to work effectively.</p>

<h3 class="mt-5">Health Programme</h3>
<p>The objectives of the school health programme are to: <br>
● Provide initial first aid and treatment of minor ailment <br>
● Administer medication when authorised to <br>
● Promote healthy lifestyles and disease prevention <br>
● Assess need and call emergency services <br>
● Organise and coordinate health screening programmes <br>
● Maintain up-to-date student medical records and vaccines.
</p>

<h3 class="mt-5">Illness at School</h3>
<p>If a child is too ill to remain in class, he/she will be sent to the sick bay. After a period of rest and assessment, the child’s parent may be called to pick up the student. Any student who develops a fever or exhibits any of the symptoms below will be sent home as soon as a parent or assigned guardian is able to pick the student: <br>
● Vomiting or diarrhea <br>
● Dizziness <br>
● Injury where there is swelling, severe pain or question of sprain or broken bone <br>
● Excessive coughing <br>
● Excessive nasal drainage <br>
● Eye injury <br>
● Rash accompanied with fever <br>
</p>

<h3 class="mt-5">Emergencies</h3>
<p>In the case of a serious emergency, the school will contact an emergency support service (HealthLink Hospital) as a first priority and thereafter, the parents as soon as possible. Parents must ensure that the school has current emergency contact details for parents/guardians and for family doctor and clinic of choice.</p>

<h3 class="mt-5">Health Policy</h3>
<p>It is expected that students with fever (>100.0 F or >38.0 C), diarrhea or vomiting may not attend school so that infectious conditions are not spread to classmates, and staff. Parents must notify the school immediately if child has contracted a contagious disease. The school will inform parents of other student who may have come into contact to the contagious disease and notify them of symptoms. Children who need to take antibiotics for an infectious disease must remain at home until they have taken a full 72 hours antibiotics to ensure that they are no longer contagious. After an ailment, a child should be symptom free for a day or two before returning to school. <br>
Chicken pox: From the first appearance of the rash until all blisters are dried and crusted over, children must not be in school.
</p>

<h3 class="mt-5">Medication at School</h3>
<p>If a child needs to take prescribed medication during school hours, regularly or for a short period of time, parents are required to meet with the school administrator and provide written prescriptions. This policy asthma inhalers. No child may self-administer medication. Medications must be stored in the students compartments or the fridge and administered by the school teacher with the supervision of the School admin.</p>

<h3 class="mt-5">Tardiness</h3>
<p>It is important to be at school on time. If you are late, you will miss class time and may disrupt the learning of others. You are tardy if you come to school after the beginning of school day or if you are not in your classroom when school day begins. If you are tardy to school, parents must sign in the attendance book and explain reason for being tardy so that student/s name may be removed from absentee list.</p>

<h3 class="mt-5">Lost and Found</h3>
<p>As a courtesy to parents and students, the school has a Lost and Found box in the school’s office. Periodically, this box will be emptied. Labelled items will be returned to students. Unidentifiable or useful items are given to a charitable organization or discarded. It is very import that all students’ clothing be marked with a complete and legible name.</p>

<h3 class="mt-5">Pick Up Persons</h3>
<p>On rare occasions both parents may be absent from home for a period of time. During this time parents must appoint a temporary guardian who is responsible for the care of their child. In doing so, parents must send a letter to the school a day before and introduce the selected temporary guardian to the school’s Director of the administrator. The letter must be a permission letter to allow the temporary guardian pick up their child, it should include the name, contact details, residential address, a valid identification card, and a passport picture. If parents or guardians fail to adhere to this, the child not be handed over to anyone but rather kept in the school and taken care of until parent/s arrives. The temporary guardian must with effect inform the school if the child is involved in an injury or accident. This is essential in order for staff to ensure the safety and well-being of students.</p>

<h3 class="mt-5">Parents as Partners</h3>
<p>Parents and teachers have a common goal for children, i.e. excellence in education. When we work together, we form a strong alliance to support a child’s wellbeing, thus emotional, social, psychological and academic growth.
Here are some ways you can help us do a better job: <br>
● See that your child attends school regularly and arrives punctually. If for any reason your child cannot attend school, inform the school of the reason for the absence. <br>
● Support the school in its efforts to maintain proper student conduct based on mutual respect, cooperation and friendship.  <br>
● Let us know if your child has any problem outside of school that might affect his/her attitude, self –esteem or schoolwork. <br>
● If you have concerns or questions about any aspects of the school programme, share them with us. Your child’s teacher can usually give you the information you want.
</p>

<h3 class="mt-5">Parents Involvement in Decision Making</h3>
<p>Parent participation in the affairs of the school is essential to maintaining a high level of educational excellence. Parents are encouraged to express ideas, concerns and suggestions to the school’s administration as such our schoolboard includes a two parents.</p>

<h3 class="mt-5">Parent-Teacher Conferences (PTC’s)</h3>
<p>The most important team in your child’s academic career is the partnership between you and your child’s teacher. Dialogue between these two parties needs to be consistent and supportive. As part of our focus on developing students who are self –directed learners with an awareness of their learning styles and strengths, and who take active roles in determining and implementing their own goals, our practice is moving toward using student portfolios in student-led conferences. Thus, the purpose of the Parent-Teacher meetings are to provide the School with assistance and recommendations that serve to enhance the quality of education and other associated activities provided by the school. PTC’s will be organized twice or thrice in a school year.</p>

<h3 class="mt-5">Visitations the school: Parents and Guardians</h3>
<p>The Family unit is of first importance to Wonder World International School since the family is the most important human institution ordained by God. The school encourages parents to take an active interest in their child’s education. Parents are always welcome to visit the school; however, for the sake of orderliness, parents should make prior arrangements through the school’s administration and/or teachers to visit at a suitable and agreed upon time. For security reasons school gates are closed after drop off throughout the day and parents are asked to identify themselves to the security guard or teacher on duty.</p>

<h3 class="mt-5">Withdrawal</h3>
<p>Written notice from parents intending to withdraw a student is required at least ten days in advance a forwarding address and last day of attendance at school must be included in the notice. Upon withdrawal, tuition is refunded for any full term (quarter) of non-attendance. Full payment is required for any portion of a school term in which a student is in attendance at the school. Ten days’ notice of intended withdrawal is required to ensure that all student records and outstanding debts are cleared.  The School will issue to parents a current report card and other records pertinent to the continuing education of the student.  </p>

<h3 class="mt-5">Tuition/Fees</h3>
<p>To ensure that tuition and other accounts are collect in a timely manner, the school has a structured plan for parents/guardians to follow. Thus, tuition/fees are payable on the first of each month or on the first day of the term. Late payment must be discussed with the school Head and a day must be agreed upon to settle outstanding fees.</p>

<h3 class="mt-5">Privacy Policy</h3>
<p>The privacy of students, staff and other members of the school is valuable, and is a matter of great importance of trust. Personal information, (including, but not limited names, addresses, phone numbers and email addresses) may only be distributed among persons belonging to the school’s community for the purposes of school-related business only and may not be distributed by Wonder World to other parties.</p>

<h3 class="mt-5">Homework</h3>
<p>The amount of homework a student needs to complete caries from day to day, from grade to grade, and from grade to grade, and from student to student. Students often need some amount of extra practice in specific new concepts, skills, or facts. In certain subjects (e.g. Math or languages), there is not enough time in a school day to do as much practice as may be necessary for mastery. Therefore, after reasonable in-class time is spent on the material, the teacher may assign homework to allow for the necessary practice.</p>

<h3 class="mt-5">Tips for helping your child with homework</h3>
<p>● Develop a routine that includes time for your child to unwind and relax for an hour before commencing homework. Coming straight from school and doing homework is not really the best strategy. A time to relax and settle in is helpful. <br>
● Provide a comfortable, well-lit space away from distractions such as younger children, computers, and television.
● Provide plenty of water to drink during homework. <br>
● Encourage your child to solve his or her own problems with homework by listening to him or her. In doing so, you allow your child to be the researcher and the problem-solver. <br>
● Praise and encourage you child. Your positivity will make a difference to your child’s approach to homework and learning in general. Simply, your presence and support creates a positive learning environment.
</p>

