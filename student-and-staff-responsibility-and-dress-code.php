            <h3 class="mt-5">Conduct Codes Concerning Alcoholic Beverages, Licit and/or Illicit Drugs</h3>
            <p>Prevention of use and abuse of alcohol and drugs by teachers is a fundamental aim of our policy. To develop knowledge, good decision-making, and strong community networks, the school begins its drug and alcohol policy with education and partnership-building among all members of our school, students, parents and staff. Teacher training programmes are designed to provide knowledge about drug use, realistic rules and boundaries, intervention strategies and support programmes.</p>

            <h3 class="mt-5">Possession and/or Use of Alcohol</h3>
            <p>Teachers or students possessing, consuming or under the influence of any alcoholic beverage on school grounds, or at any school organized event are subject to disciplinary consequences as described below.  If the teacher or student is suspended, the duration of the suspension will be determined by the school administration and will be based on the severity of the offence. Any teacher or student who has a history of disciplinary problems may be requested to withdraw from the school or face expulsion. The School’s Director (or his/her designee) has the responsibility and authority to decide what constitutes sufficient cause or appropriate circumstances to warrant a search for possession of alcohol. </p>

            <h3 class="mt-5">Use of Illegal Drugs and Misuse of Controlled Substances</h3>
            <p>Students/staff buying, selling, possessing, taking, giving or using or in any way directly or indirectly aiding or abating the using of illegal drugs, misuse of controlled substances (both prescription and over the counter medicines, including psychoactive substances or performance enhancing drugs, stimulants, depressants, narcotics, cannabis, hallucinogens, inhalants, designer drugs or anabolic steroids) or uncontrolled substances (such as glues and solvents) on school grounds, or at any organized school event or trip are all strictly prohibited. Such behaviour in many cases is also illegal in the country and the school may be required to file a report with the local authority. Students/staff who are reasonably suspected based on specific factors and circumstances to be in any way involved in any of the above behaviour are subject to disciplinary consequences as described below, including being requested to withdraw or being expelled from the school. The school’s director (or his/her designee) has the responsibility and authority to describe what constitutes sufficient course or appropriate circumstances to warrant a search for possession and/or to require testing for possible use of illegal drugs or misuse of controlled substances.</p>

            <h3 class="mt-5">Authorised Drugs</h3>
            <p>Parents of students who are required to take authorized drugs must submit written authorization together with the said drug and instructions for dispensation to the school.</p>

            <h3 class="mt-5">Dress Code (Must be strictly adhered to)</h3>
            <p>All students are required to observe the following regulations: 
                <br>
● Students are responsible for observing basic standards of cleanliness, modesty, good grooming and dress. <br>
● Students are to wear the prescribed school uniform and a pair of cream/white socks and a black full comfortable shoe or standards. Hair must always be kept tidy. <br>
● Students are to wear the prescribed sports outfit and a pair of white socks with black or white sneakers. <br>
● Girls must avoid weave-ons, wigs, colored braids, dying of hair and acrylic nails. The natural hair color should be kept. Bangles, chains, dangling earrings and ankle chains are also not acceptable. <br>
● Boys are not allowed to plait, curl or dye their hair, wear nose and earrings, bangles, chains and rings. <br>
● Hearts and caps are not permitted indoors. <br>
● Clothing or accessories with suggestive, offensive or otherwise objectionable messages are not permitted. <br>
● Any student dressed in our uniform either on or off our premises is considered an ambassador of the school. Therefore they must conduct themselves as much or will be punished for any misconduct. Our policy requires 100% compliance with regards to school uniforms and dress codes. Pupils wearing irregular items will have them confiscated and not to be returned. The student will be suspended from the school until compliance is met.
</p>

            <h3 class="mt-5">Fighting or Physical Aggression</h3>
            <p>Fighting or Physical Aggression is not permitted on or near campus, or on any school sponsored or organized event. A student who is attacked may use reasonable force in self-defense but only to the extent necessary to get free from attack and notify proper school authorities. A student who exceeds reasonable force may be expelled even though someone else provoked the fight. Where it is possible to determine the original motivation or argument resulting to the fight, all parties will be held equally responsible. No student may cause or attempt to cause physical injury to any student or intentionally behave in such a manner that could reasonably cause physical injury to others.</p>

            <h3 class="mt-5">Items Disallowed in School</h3>
            <p>The following items are not allowed in school: mobile phones, compact disc and players, knives and matches. Mobile phones are not be used during the school day. The School is not responsible for the loss, theft or damage of students’ personal items. Lockers are provided at the secondary level and students are advised to keep valuables locked at all times. Tape recorders and laptops are only allowed for specific school assignments.</p>

            <h3 class="mt-5">Off-Campus Situations</h3>
            <p>Should the school’s administration be made aware of any students violating the code of conduct outside of school hours or at non-school-related activities in ways that affect students’ ad community well-being on campus, the administration reserves the right to evaluation such situations and discuss them with parents and students. Steps may be taken for supportive resolution of the situation. Otherwise, said students may be expelled from the school.</p>

            <h3 class="mt-5">Possession/Use of Weapons</h3>
            <p>No student ay possess, use or distribute weapon(s) on school grounds, on school transport or at any school-organised or sponsored event or trip. A weapon is defined as nay object, device or instrument designed as a weapon or through its use is capable of threatening or producing bodily harm or inflicting self-injury including, but not limited to, any firearm, whether loaded or unloaded; air guns, pellet guns or BB guns; all knives or blades; explosives or fireworks; mace and other propellants; stun guns; ammunition; poisons; and objects that have been modified to serve as a weapon. Any object, device or instrument having the appearance of a weapon may also be considered a weapon. Items designed for other purposes (i.e. files, scissors, a pair of compass, pens, etc.), used to inflict bodily harm and/or intimidate will be treated as possession and use of a weapon. Students in violation of this policy are subject to disciplinary consequences as described above, including withdrawal or expulsion from school.</p>

            <h3 class="mt-5">Profanity, insolence, Disobedience, and Foul Language</h3>
            <p>Profanity, insolence, disobedience, or foul language will not be tolerated. Any student in violation will be reported to the department heads for immediate action. A second offence may result in suspension from school. Parents will be notified in writing of the incidents(s).</p>

            <h3 class="mt-5">Theft and Vandalism</h3>
            <p>Any student caught stealing or vandalising school property will be reported to the department head and may be subject to disciplinary consequences as described below. In serious and repeated cases, the student may be requested to withdraw from school or face expulsion. Parents will be notified in writing of the incident(s).</p>

            <h3 class="mt-5">Truancy/Unauthorized Departure from School</h3>
            <p>A student who leaves the school premises without parents’ authorization and the permission of the administration shall not be readmitted to school without a parent/student conference with the appropriate principal. Truancy cases will be handled in a similar manner. The department heads may enforce other disciplinary consequences as described above.</p>

            <h3 class="mt-5">Field Trips</h3>
            <p>The school recognizes the importance of school-sponsored trips as a means of re-enforcing and supplementing the curriculum. Trips in and beyond the community provide a direct learning experience and opportunities for practical application. A field trip is a planned educational activity in which a class or a group of students leaves the School’s campus for the purpose of continuing, extending or enriching the instructional programme. A trip is designed to stimulate student interest and inquiry and provide opportunities for social growth and development. To be educationally beneficial, a field trip requires thoughtful selection, careful advance preparation of the students and opportunities for the students to assimilate the experience during and at the conclusion of the trip.</p>
            <p>School Field Trips Are to: <br>
● Provide authentic, direct exposure to local and surrounding areas in order nurture a better understanding and appreciation of history, geography and culture. <br>
● Develop new friendship as well as strengthen the existing bonds the students have within the school community. <br>
● Allow for opportunities that are relevant to the needs of the students’ education. <br>
● Provide an opportunity to understand the importance of knowledge and understanding in our contemporary world. <br>
● Increase respect for the environment and local culture through exposure to unique habitats <br>
● Provide a framework for students to be involved in meaningful service projects. <br>
● Provide opportunities for personal reflection, self-awareness and reliance. <br>
● Provide opportunities to develop new interests and aspirations <br>
● Provide opportunities to develop leadership skills.
</p>

<p>The Following Guidelines Pertain to School Field Trips: <br>
● Field trip venues and logistics must be previewed or recommended based on personal experience by at least one staff member <br>
● Only buses and not private cars should be used for transportation of students. When private cars are the best option, authorization must be granted by the school administration <br>
● For extended trips, a minimum of two members of staff, one male and one female, are required. For local day trips, the school may request assistance from parents. <br>
● A first aid kit is required for all field trips and staff should be familiar with contents and use. Any prescriptive medication must remain in its original container with directions on how to administer. <br>
● All students require written permission from parent/guardian to leave the campus.
</p>

<p>Appropriate Behaviour for Students and Teachers on Field Trips: <br>
● Teachers will reinforce behaviour expectations for students prior to the field trip. All school rules apply to any-off campus activity. No student may consume alcohol or smoke, or use illicit drugs regardless of the environment. <br>
● Activities that end themselves to injuries are not permitted without prior permission from the school administration. Such activities include swimming, hiking and climbing, driving any type of vehicle, etc. Teachers should thoroughly discuss their plans with the school principal and School Director to determine if any precautions are necessary. <br>
● It is expected that teachers will maintain continuous supervision of all students – that is, no student should be left unattended or out-of-sight for any reason. <br>
● Teachers must exhibit only the most professional behaviours at all times.
</p>
